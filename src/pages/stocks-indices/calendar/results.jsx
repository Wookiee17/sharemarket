import React, { useEffect, useState } from "react";
import Head from "next/head";
import dayjs from "dayjs";

// layout
import Layout from "../../../common/layouts/Layout";
import {
  setActiveTab,
  setActiveCalendarInnerTab,
} from "../../../store/stockIndices/StockIndicesSlice";

// redux
import { useDispatch, useSelector } from "react-redux";
import { handleChangeData } from "../../../store/calendar/CalendarSlice";

// container
import StocksIndicesLayout from "../../../containers/stocksindices/StocksIndicesLayout";

// component
import Search from "../../../common/component/search/Search";
import TopTab4 from "../../../common/component/stocksindices/top-tab4/TopTab4";
import CalendarLayout from "../../../common/component/stocksindices/tabs/tab4/calendarLayout/CalendarLayout";

//graphql
import { useQuery } from "@apollo/client";
import { GET_BOARD_MEETINGS } from "../../../gql/queries";

const Results = () => {
  const dispatch = useDispatch();
  const { daterange } = useSelector((state) => state.Calendar);
  useEffect(() => {
    dispatch(setActiveTab(3));
    dispatch(setActiveCalendarInnerTab(0));
  }, []);

  const [value, setValue] = useState("");

  useQuery(GET_BOARD_MEETINGS, {
    variables: {
      where: {
        _and: [
          { meeting_date: { _gte: dayjs(daterange[0]).format("YYYY-MM-DD") } },
          {
            meeting_date: {
              _lte: dayjs(daterange[daterange.length - 2]).format("YYYY-MM-DD"),
            },
          },
          {
            _or: [{
              remarks: { _ilike: `%${value}%` },
            }, {
              company: {
                name: { _ilike: `%${value}%` }
              }
            },{
              purpose: { _ilike: `%${value}%` },
            }]
          }
          // { company: { name: { _ilike: `%${value}%` } } },
        ],
      },
    },
    onCompleted: (data) => {
      if (Array.isArray(data?.meetings)) {
        dispatch(
          handleChangeData({
            key: "results",
            value: data.meetings.map((d) => {
              return {
                title: d.company?.name,
                subTitle: d.purpose,
                date: d.meeting_date,
                payload: d,
              };
            }),
          })
        );
      }
    },
    skip: daterange.length === 0,
  });
  return (
    <Layout>
      <StocksIndicesLayout>
        <Head>
          <title>Stock Indices | IC Trading</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <div className="tab4">
          <div className="left">
            <div className="left_headersearch">
              <h1>CALENDAR</h1>
              <div>
                <Search
                  placeholderText={"Enter Keyword"}
                  onChange={(e) => setValue(e.target.value)}
                />
              </div>
            </div>
          </div>
          <div className="content">
            <TopTab4 />
            <div className="right">
              <CalendarLayout page="results" />
            </div>
          </div>
        </div>
      </StocksIndicesLayout>
    </Layout>
  );
};

export default Results;
